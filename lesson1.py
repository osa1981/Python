if (rev > costs):
    rent = (rev - costs) / rev  # Вычисляет рентабельность выручки
    print(f'Ваше предприятие принесло прибыль, рентабельность выручки составила {round(rent, 3)}')
    empl = int(input('Введите численность персонала '))  # Запрашивает число сотрудников
