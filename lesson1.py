# Пользователь вводит целое положительное число.
# Найдите самую большую цифру в числе.
# Для решения используйте цикл while и арифметические операции.
numN = int(input('Введите целое положительное число '))
n = 0  # разряд числа
m = 0  # наибольшая цифра
while ((numN // (10 ** n)) != 0):  # проверяет, достигнуто ли начало значимой части числа
    rank = int(((numN % (10 ** (n + 1))) - numN % (10 ** n)) / (10 ** n))  # вычисляет цифру в n-м разряде
    if (rank >= m):  # сравнивает цифру n-го разряда с текущим максимумом
        m = rank  # фиксирует новый максимум
    n += 1
print(f'Наибольшая цифра в числе {numN} равна {m}')
print('*' * 12)
